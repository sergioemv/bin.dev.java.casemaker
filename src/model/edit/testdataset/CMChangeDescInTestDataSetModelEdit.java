package model.edit.testdataset;

import javax.swing.undo.AbstractUndoableEdit;
import javax.swing.undo.CannotRedoException;
import javax.swing.undo.CannotUndoException;

import model.TestDataSet;

/**
 * @author svonborries
 *
 */
@SuppressWarnings("serial")
public class CMChangeDescInTestDataSetModelEdit extends AbstractUndoableEdit {

	
	private TestDataSet m_tds;
	private String newDesc;
	private String oldDesc;

	public CMChangeDescInTestDataSetModelEdit(TestDataSet p_tds, String p_des){
		m_tds = p_tds;
		newDesc = p_des;
		oldDesc = p_tds.getDescription();
	}
	
	/* (non-Javadoc)
	 * @see javax.swing.undo.AbstractUndoableEdit#canRedo()
	 */
	@Override
	public boolean canRedo() {
		if(m_tds!=null)
			return true;
		return false;
	}

	/* (non-Javadoc)
	 * @see javax.swing.undo.AbstractUndoableEdit#canUndo()
	 */
	@Override
	public boolean canUndo() {
		if(m_tds!=null)
			return true;
		return false;
	}

	/* (non-Javadoc)
	 * @see javax.swing.undo.AbstractUndoableEdit#redo()
	 */
	@Override
	public void redo() throws CannotRedoException {
		super.redo();
		m_tds.setDescription(newDesc);
	}

	/* (non-Javadoc)
	 * @see javax.swing.undo.AbstractUndoableEdit#undo()
	 */
	@Override
	public void undo() throws CannotUndoException {
		super.undo();
		m_tds.setDescription(oldDesc);
	}

}
